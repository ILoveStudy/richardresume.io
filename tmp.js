/**
 * we know
 * f(0) = 0;f(1) = 1;
 * f(n) = f(n-1) + f(n-2);
 *****************************************
 * when n = 1;
 * f(1) = f(0) + f(-1);
 *
 * so
 * f(-1) = 1;f(0) = 0;f(1) = 1
 *
 ******************************************
 * when n = 2
 *
 * f(2) = f(1) + f(0)
 *
 * so
 * f(-1) = 1;f(0) = 0;f(1) = 1; f(2) = 1;
 ******************************************
 * when n = 3;
 *
 * f(3) = f(2) + f(1);
 *
 * f(-1) = 1;f(0) = 0;f(1) = 1; f(2) = 1;f(3) = 2;
 *
 * ****************************************
 * when n = 4;
 *
 * f(4) = f(3) + f(2);
 *
 * f(-1) = 1;f(0) = 0;f(1) = 1; f(2) = 1;f(3) = 2;f(4) = 3;
 *
 * *****************************************
 *
 * when n = 5;
 *
 * f(5) = f(4) + f(3);
 *
 * f(-1) = 1;f(0) = 0;f(1) = 1; f(2) = 1;f(3) = 2;f(4) = 3;f(5) = 5;
 *
 * etc.
 * .......
 * .......
 * .......
 */

// javascript

function f(n) {

    var fn_1 = 0, fn_2 = 1, fn;

    if (n == 0) {
        return 0;
    } else if (n == 1) {
        return 1;
    }

    for (var i = 2; i <= n; i++) {
        /**
         * we know: f(n) = f(n-1) + f(n - 2);
         * when n = 2;
         * f(2) = f(1) + f(0);
         */
        fn = fn_1 + fn_2;
        fn_1 = fn_2;
        fn_2 = fn;
    }
    return fn_2;
}

